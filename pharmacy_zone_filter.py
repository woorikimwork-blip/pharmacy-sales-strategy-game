# ═══════════════════════════════════════════════════════════════
# 마스크 생성 함수
# ═══════════════════════════════════════════════════════════════
def mask_keywords(df, keywords):
    m = pd.Series(False, index=df.index)
    for kw in keywords:
        m |= df['_주소'].str.contains(kw, na=False)
    return m

m_t = mask_keywords(df, tourist_keywords)
m_h = mask_keywords(df, hightraffic_keywords)
m_m = mask_keywords(df, medical_keywords)

# ═══════════════════════════════════════════════════════════════
# 우선순위 적용: 성형·피부과 > 외국인관광 > 유동인구
# pharmacy_map.py와 동일 로직 - 각 약국은 단 하나의 상권에만 배정
# ═══════════════════════════════════════════════════════════════
def assign_zone(t, h, m):
    if m: return '성형·피부과 집중 상권'
    if t: return '외국인 관광객 상권'
    if h: return '유동인구 많은 상권'
    return None

df['상권유형'] = [assign_zone(t, h, m) for t, h, m in zip(m_t, m_h, m_m)]
df_zone = df[df['상권유형'].notna()].copy()

# 매칭 상권 태그 (해당 카테고리 키워드 중 실제 매칭된 것)
kw_map = {
    '외국인 관광객 상권':    tourist_keywords,
    '유동인구 많은 상권':    hightraffic_keywords,
    '성형·피부과 집중 상권': medical_keywords,
}

def get_matched_tag(row):
    kws = kw_map[row['상권유형']]
    matched = [kw for kw in kws if kw in str(row['_주소'])]
    return ', '.join(matched[:2])

df_zone['매칭_상권'] = df_zone.apply(get_matched_tag, axis=1)

# 출력 컬럼
output_cols = ['사업장명', '소재지전체주소', '도로명전체주소', '소재지전화',
               '인허가일자', '상세영업상태명']
output_cols = [c for c in output_cols if c in df.columns]
final_cols  = output_cols + ['매칭_상권']

df_tourist_out     = df_zone[df_zone['상권유형'] == '외국인 관광객 상권'][final_cols].reset_index(drop=True)
df_hightraffic_out = df_zone[df_zone['상권유형'] == '유동인구 많은 상권'][final_cols].reset_index(drop=True)
df_medical_out     = df_zone[df_zone['상권유형'] == '성형·피부과 집중 상권'][final_cols].reset_index(drop=True)
df_all             = df_zone[output_cols + ['매칭_상권', '상권유형']].reset_index(drop=True)

print(f'① 외국인 관광객 상권: {len(df_tourist_out)}개')
print(f'② 유동인구 많은 상권: {len(df_hightraffic_out)}개')
print(f'③ 성형·피부과 집중 상권: {len(df_medical_out)}개')
print(f'   ※ 우선순위 적용 (성형>관광>유동) - 중복 약국 단일 카테고리 배정')

# 엑셀 저장 (4개 시트)
with pd.ExcelWriter('약국_상권별_리스트.xlsx', engine='openpyxl') as writer:
    df_tourist_out.to_excel(writer,     sheet_name='①외국인관광객상권',  index=False)
